.MODEL SMALL 
.STACK 100 
.DATA
 ITEM1 DB 1,2,3 ;ITEM1 DB 3 DUP(0)
 
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX  

;---CLEAR AX (IMPORTANT STEP)
 MOV AX,0   ;AX=0 , SAME AS :SUB AX,AX /XOR AX,AX (SAME METHOD)                
;---USE DIRECT ADDRESSING
;---TO ALL ITEM1[0],ITEM1[1],ITEM1[2]
 ADD AL,ITEM1[0] ;AL=AL+ITEM1[0]=0+1=1
 ADD AL,ITEM1[1] ;AL=AL+ITEM1[1]=1+2=3
 ADD AL,ITEM1[2] ;AL=AL+ITEM1[2]=3+3=6
;---DEC > HEX
 ADD AL,30H      ;AL=36H
;---OUTPUT BYTE:02H
 MOV AH,02H
 MOV DL,AL ;DL=AL=36H=ASCII-6
 INT 21H


 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN