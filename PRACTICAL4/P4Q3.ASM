.MODEL SMALL 
.STACK 100 
.DATA 
 VAR1 DB "COMPUTER"
 VAR2 DB 8 DUP ("*")
 STR1 DB " (1) ORIGINAL$"
 STR2 DB 13,10,13,10, " (2) AFTER EXCHANGE$"
 STR3 DB 13,10,13,10," (3) AFTER CHANGE CASE$"
 STR4 DB 13,10,13,10," (4) AFTER REVERSE$"
 VSTR1 DB 13,10,"VAR1 = $"
 VSTR2 DB 13,10,"VAR2 = $"
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX
;-----------------------------(1)ORIGINAL---------;
 ;--OUTPUT STRING:09H(STR1)
 MOV AH,09H
 LEA DX,STR1
 INT 21H
 ;--OUTPUT STRING:09H(VSTR1)
 MOV AH,09H
 LEA DX,VSTR1
 INT 21H
 ;--OUTPUT BYTE:02H (VAR1[SI]), USING LOOP PRINT1
 MOV CX,8
 MOV SI,0
PRINT1:
 MOV AH,02H
 MOV DL,VAR1[SI] ;DL="C","O","M","P","U","T","E","R"
 INT 21H
 INC SI         ;SI=SI+1 ,SI=1>2>3>4>5>6>7>8
 LOOP PRINT1    ;CX=CX-1 ,CX=7>6>5>4>3>2>1>0 WHEN UNTIL 0 THEN END
 ;--OUTPUT STRING:09H(VSTR2)
 MOV AH,09H
 LEA DX,VSTR2
 INT 21H
 ;--OUTPUT BYTE:02H (VAR2[SI]), USING LOOP PRINT2
 MOV CX,8
 MOV SI,0
PRINT2:
 MOV AH,02H
 MOV DL,VAR2[SI]
 INT 21H
 INC SI
 LOOP PRINT2

;-------------------------------------------(2)EXCHANGE(XCHG)
 ;--OUTPUT STRING:09H(STR2)
  MOV AH,09H
  LEA DX,STR2
  INT 21H
 ;--OUTPUT STRING:09H(VSTR1)
  MOV AH,09H
  LEA DX,VSTR1
  INT 21H
 ;--EXCHANGE VAR1[SI] WITH VAR2[SI]--; LOOP
 MOV CX,8
 MOV SI,0
 SWAP:
  MOV BL,VAR1[SI] ;BL=VAR1[SI] ,COMPUTER |BL=C
  XCHG BL,VAR2[SI] ;BL=C | VAR2[SI]=*  $SWAP ;VAR2[SI]=C | BL=*
  XCHG BL,VAR1[SI] ;BL=* | VAR1[SI]=C  $SWAP ;VAR1[S1]=* | BL=C
  ;MOV BL,VAR1[SI]  ;ASSIGN VAR1[S1]=* TO BL 
  ;MOV VAR1[SI],BL  ;ASSIGN BL TO VAR1[S1]
  INC SI
  LOOP SWAP
  
 ;--OUTPUT BYTE:02H(VAR1[SI]), USING LOOP PRINT1
 MOV CX,8
 MOV SI,0
PRINT11:
  MOV AH,02H
  MOV DL,VAR1[SI]
  INT 21H
  INC SI
  LOOP PRINT11
 ;--OUTPUT STRING:09H(VSTR2)
 MOV AH,09H
 LEA DX,VSTR2
 INT 21H
 ;--OUTPUT BYTE:02H(VAR2[SI]), USING LOOP PRINT2
 MOV CX,8
 MOV SI,0
PRINT22:
  MOV AH,02H
  MOV DL,VAR2[SI]
  INT 21H
  INC SI
  LOOP PRINT22
;-------------------------------------------(3)CHANGE CASE
 ;--OUTPUT STRING:09H(STR3)
 MOV AH,09H
 LEA DX,STR3
 INT 21H
 ;--OUTPUT STRING:09H(VSTR1)
 MOV AH,09H
 LEA DX,VSTR1
 INT 21H
 ;--SWAP BACK VALUE
 MOV CX,8
 MOV SI,0
 SWAPBACK:
 MOV BL,VAR1[SI]
 XCHG BL,VAR2[SI]
 XCHG BL,VAR1[SI]
 INC SI
 LOOP SWAPBACK
 ;--OUTPUT BYTE:02H(VAR1[SI]), USING LOOP PRINT1
 MOV CX,8
 MOV SI,0
 PRINT111:
  MOV AH,02H
  MOV DL,VAR1[SI]
  INT 21H
  INC SI
  LOOP PRINT111
 ;--OUTPUT STRING:09H(VSTR2)
 MOV AH,09H
 LEA DX,VSTR2
 INT 21H
 ;--CHANGE CASE: ADD 20H TO VAR1[SI], THEN MOVE TO VAR2[SI]
 MOV CX,8
 MOV SI,0
 MOV AX,0
 ADD1: ;TRY TO USE ANOTHER REGISTER(BL) TO STORE VALUE , IF USE AL, REMEMBER CLEAR IT
  MOV AL,VAR1[SI]
  ADD AL,20H
  MOV VAR2[SI],AL
  INC SI
  LOOP ADD1
 ;--OUTPUT BYTE:02H(VAR2[SI]), USING LOOP PRINT2
 MOV CX,8
 MOV SI,0
 PRINT222:
  MOV AH,02H
  MOV DL,VAR2[SI]
  INT 21H
  INC SI
  LOOP PRINT222


;-------------------------------------------(4)REVERSE
;--OUTPUT STRING:09H(STR4)
 MOV AH,09H
 LEA DX,STR4
 INT 21H
;--OUTPUT STRING:09H(VSTR1)
 MOV AH,09H
 LEA DX,VSTR1
 INT 21H
;--OUTPUT BYTE:02H(VAR1[SI]), USING LOOP PRINT1
MOV CX,8
MOV SI,0
PRINT1111:
 MOV AH,02H
 MOV DL,VAR1[SI]
 INT 21H
 INC SI
 LOOP PRINT1111
;--OUTPUT STRING:09H(VSTR2)
 MOV AH,09H
 LEA DX,VSTR2
 INT 21H
;      SI  0 1 2 3 4 5 6 7
; VAR1[SI] C O M P U T E R
;      SI INITIALIZED WITH 7
;      SI DECREMENT (DEC)
;SWAP VAR2[SI] TO BIG CASE (minus)
MOV CX,8
MOV SI,0
MOV AX,0 ;THIS IMPORTANT
MINUS: 
 MOV AL,VAR2[SI]
 SUB AL,20H
 MOV VAR2[SI],AL 
 INC SI
 LOOP MINUS
; VAR2[DI] R E T U P M O C
;      DI  0 1 2 3 4 5 6 7
;      DI INITIALIZED WITH 0
;      DI INCREMENT
;--OUTPUT BYTE:02H(VAR1[SI]), USING LOOP PRINT1
MOV CX,8
MOV DI,7 ;cannot put 8 because array start from 0-7
PRINT2222:
 MOV AH,02H
 MOV DL,VAR2[DI]
 INT 21H
 DEC DI ;decrease
 LOOP PRINT2222

 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN