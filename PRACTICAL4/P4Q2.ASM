.MODEL SMALL 
.STACK 100 
.DATA 

.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX 
;--OUTPUT 256 ASCII CODE
 MOV CX,256 ;256 LOOP    
 MOV BL,0 ;BECAUSE BX IS TWO BYTES
PRINT:
 MOV AH,02H
 MOV DL,BL 
 INT 21H
 INC BL
 LOOP PRINT

 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN