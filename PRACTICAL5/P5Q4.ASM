.MODEL SMALL 
.STACK 100 
.DATA
 STR1 DB "ENTER A WORD: $"
 STR2 DB 13,10,"THE SECOND CHARACTER IS $"
 ARRAY      LABEL      BYTE
 MAX         DB         10
 ACT         DB          ?
 ARRAYDATA   DB         10 DUP(0)
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX  

;---OUPUT STRING:09H(STR1) 
 MOV AH,09H
 LEA DX,STR1
 INT 21H
;---INPUT STRING:0AH(ARRAY)  
 MOV AH,0AH 
 LEA DX,ARRAY
 INT 21H
;---OUTPUT STRING:09H(STR2)
 MOV AH,09H
 LEA DX,STR2
 INT 21H
;---OUTPUT BYTE:02H(ARRAYDATA[1])
 MOV AH,02H
 MOV DL,ARRAYDATA[1]
 INT 21H
 
 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN
