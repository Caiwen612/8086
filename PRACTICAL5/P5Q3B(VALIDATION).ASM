.MODEL SMALL 
.STACK 100 
.DATA
 QTY DB 0
 IMSG DB 13,10,"INVALID INPUT! ENTER DIGIT ONLY!$"
 VMSG DB 13,10,"CONTINUE CALC....$"
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX
START:
;---INPUT BYTE:01H   
 MOV AH,01H
 INT 21H
 MOV QTY,AL
;--VALIDATION:"0" - "9"
CHECK: 
 CMP QTY,"0"
 JAE CHK2
 JB PRINTI

CHK2:
 CMP QTY,"9"
 JBE PRINTV
 JA PRINTI

PRINTI:
 MOV AH,09H
 LEA DX,IMSG
 INT 21H
 JMP START

PRINTV:
 MOV AH,09H
 LEA DX,VMSG
 INT 21H
 JMP EXIT




EXIT: 
 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN