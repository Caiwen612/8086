; TO MARK THE END OF THE STRING/ELEMENT: "$"
; TO MARK THE END OF THE STRING/ELEMENT: CX
; TO MARK THE END OF THE STRING/ELEMENT: CMP SI
.MODEL SMALL
.STACK 100
.DATA
    UNAME DB "BACS"
    VMSG  DB 13,10, "ACCESS GRANTED$"
    IMSG  DB 13,10, "INVALID LOGIN$"
    INAME DB 4 DUP (0)
.CODE
MAIN PROC
   MOV AX,@DATA 
   MOV DS,AX

;----GET INAME
    MOV CX,4 
    MOV SI,0
GETUSERNAME:
 MOV AH,01H
 INT 21H
 MOV INAME[SI],AL
 INC SI
 LOOP GETUSERNAME

;------CHECKING
MOV SI,0
CHK:
    CMP SI,4
    JE PRINT ;=
    JNE CHK2 ;!=

CHK2:
    MOV BL,UNAME[SI]
    CMP BL,INAME[SI]
    JE NEXT
    JNE ERROR

NEXT:
    INC SI
    JMP CHK

ERROR:
    MOV AH,09H
    LEA DX,IMSG
    INT 21H
    JMP EXIT

PRINT:
    MOV AH,09H
    LEA DX,VMSG
    INT 21H
    JMP EXIT

EXIT:
    MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN