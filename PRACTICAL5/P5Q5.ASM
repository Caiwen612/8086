; ASCII-5 = 35H = 0011 0101B
; ASCII-7 = 37H = 0011 0111B AND
;                              0011 0101B = 35H (ASCII-5)
;NOTED
; BEFORE DO CALCULATION 
; HEX > DEC
; -30H

; AFTER DO CALCULATION
; DEC>HEX
; +30H
.MODEL SMALL 
.STACK 100 
.DATA
   MENU DB "ENTER THE OPTION NUMBER (1,2,3 OR 4) FOR "
        DB "OPERATION"
        DB 13,10,"1. AND"
        DB 13,10,"2. OR"
        DB 13,10,"3. XOR"
        DB 13,10,"4. NOT"
        DB 13,10,"------------"
        DB 13,10,"OPERATION SELECTION: $"
   STRX DB 13,10,"ENTER VALUE FOR X: $"
   STRY DB 13,10,"ENTER VALUE FOR Y: $"
STR1 DB 13,10," X AND Y = $"
STR2 DB 13,10," X OR  Y = $"
STR3 DB 13,10," X XOR  Y = $"
STR4 DB 13,10," NOT X  = $"
OPT DB 0
X DB 0
Y DB 0
ANS DB 0
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX 
;---OUTPUT  STRING:09H(MENU)    
 MOV AH,09H
 LEA DX,MENU
 INT 21H
;---INPUT BYTE:01H(OPT)
 MOV AH,01H
 INT 21H
 MOV OPT,AL
;--CHECKING
 CMP OPT,"1"
 JE GETXY
 CMP OPT,"2"
 JE GETXY
 CMP OPT,"3"
 JE GETXY
 CMP OPT,"4"
 JE GETX

GETXY:
 MOV AH,09H
 LEA DX,STRX
 INT 21H

 MOV AH,01H
 INT 21H
 SUB AL,30H
 MOV X,AL

 MOV AH,09H
 LEA DX,STRY
 INT 21H

 MOV AH,01H
 INT 21H
 SUB AL,30H
 MOV Y,AL
 JMP CALC

GETX:
 MOV AH,09H
 LEA DX,STRX
 INT 21H

 MOV AH,01H
 INT 21H
 SUB AL,30H
 MOV X,AL
 JMP CALC

CALC:
 CMP OPT,"1"
 JE DOAND 
 CMP OPT,"2"
 JE DOOR   
 CMP OPT,"3"
 JE DOXOR
 CMP OPT,"4"
 JE DONOT

DOAND:
 MOV BL,X
 AND BL,Y
 MOV ANS,BL
 ADD ANS,30H

 MOV AH,09H
 LEA DX,STR1
 INT 21H

 MOV AH,02H
 MOV DL,ANS
 INT 21H
 JMP EXIT
DOOR:
 MOV BL,X
 OR BL,Y
 MOV ANS,BL
 ADD ANS,30H

 MOV AH,09H
 LEA DX,STR2
 INT 21H

 MOV AH,02H
 MOV DL,ANS
 INT 21H
 JMP EXIT
DOXOR:
 MOV BL,X
 XOR BL,Y
 MOV ANS,BL
 ADD ANS,30H

 MOV AH,09H
 LEA DX,STR3
 INT 21H

 MOV AH,02H
 MOV DL,ANS
 INT 21H
 JMP EXIT
DONOT:
 MOV BL,X
 NOT BL
 MOV ANS,BL
 ADD ANS,30H

 MOV AH,09H
 LEA DX,STR4
 INT 21H

 MOV AH,02H
 MOV DL,ANS
 INT 21H
 JMP EXIT




 
EXIT:
 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN