.MODEL SMALL 
.STACK 100 
.DATA
 STR1 DB "THERE ARE $"
 STR2 DB " POSITIVE AND $"
 STR3 DB " NEGATIVE VALUE IN THE LIST$"
 LIST DB 2,-4,6,8,-2,0
 PCOUNT DB 30H
 NCOUNT DB 30H
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX  
;-CALCULATE POSITIVE & NEGATIVE VALUES   
 MOV SI,0
CHK: ;BECAUSE SIGN NUMBER SO USE JG AND JL, CANNOT USE JA AND JB BECAUSE NUMBER DID NOT HAVE UNSIGN NUMBER
 CMP LIST[SI],0
 JE PRINT ;IF LIST[SI] =0
 JG POSITIVE  ;>0
 JL NEGATIVE  ;<0

POSITIVE:
 INC PCOUNT
 INC SI
 JMP CHK

NEGATIVE:
 INC NCOUNT
 INC SI
 JMP CHK

PRINT:
 MOV AH,09H
 LEA DX,STR1
 INT 21H

 MOV AH,02H
 MOV DL,PCOUNT
 INT 21H

 MOV AH,09H
 LEA DX,STR2
 INT 21H

 MOV AH,02H
 MOV DL,NCOUNT
 INT 21H

 MOV AH,09H
 LEA DX,STR3
 INT 21H

 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN