.MODEL SMALL 
.STACK 100 
.DATA
 CHAR DB ? ;OR {CHAR DB 0}
;DELCARE CHAR AS A VARIABLE(CAN USE WHAT NAME YOU LIKE) (DB IS SIZE) , IF DK WHAT USER WILL KEY IN, CAN USE 0 OR ? as a null
.CODE 

MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX

 ;---OUTPUT BYTE:02H
 MOV AH,02H ;OUTPUT FUNCTION ;COUT
 MOV DL,03H ; DISPLAY LOVE SHAPE , DL=HOLD THE BYTE TO BE DISPLAYED
 ;MOV DL,02H ;DISPLAY SMILE
 ;MOV DL,"," ; DISPLAY THE DOUBLE QUOTE CHARACTER
 INT 21H

 ;--INPUT BYTE:01H
 MOV AH,01H ;input
 INT 21H ;AL = AUTO HOLD USER INPUT BYTE. ANOTHER WORD IS AL TO HOLD THE INPUT DATA
 MOV CHAR,AL ;THE VALUE WILL STORE IN CHAR VARIABLE

 ;--INPUT BYTE:07H (USER INPUT IS *******) (USE FOR PASSWORD)(you will dk how many you key in)
 MOV AH,07H ;Input password
 INT 21H ;AL = AUTO HOLD USER INPUT BYTE. ANOTHER WORD IS AL TO HOLD THE INPUT DATA

 ;--FORCE DISPLAY (TO DISPLAY OUTPUT) (done)
 MOV AH,02H
 MOV DL,"*"
 ;MOV DL,AL ;MOVE AL TO DISPLAY
 INT 21H

 ;--OUTPUT BYTE:02H
 MOV AH,02H
 MOV DL,CHAR 
 INT 21H


 MOV AH,4CH ;FUNCTION CODE
 INT 21H ;INT=INTERUPT, TERMINATE PROGRAM
MAIN ENDP ;END THE PROCETURE 
 END MAIN


 