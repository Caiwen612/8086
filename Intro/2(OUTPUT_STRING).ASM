.MODEL SMALL 
.STACK 100 
.DATA
 MSG1 DB "HELLO$"
 MSG2 DB "ASSEMBLY" ; CAN TRY WITHOUT DOLLAR SYMBOL
 MSG3 DB "LANGUAGE$" ;$=END OF STRING
 MENU DB  0DH,0AH,13,10,"---FOODS---"
      DB  13,10,"1. BURGERS"
      DB  13,10,"2. PIZZAS"
      DB  0DH,0AH,"3. FIRES$"

.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX

 ;--OUTPUT STRING:09H
 MOV AH,09H
 LEA DX,MSG1 ;DISPLAY STRING ,START FROM ADDRESS MSG1 ;LEA TELL COMPUTER WHERE TO START, WE NEED TO STOP SO USE DOLLAR SIGN
 INT 21H

 ;--NEWLINE 
 MOV AH,02H
 MOV DL,0DH
 INT 21H
 MOV DL,0AH
 INT 21H

 ;--OUTPUT STRING:09H
 MOV AH,09H
 ;LEA = (Load Effective Address)
 LEA DX,MSG2 ;DISPLAY STRING ,START FROM ADDRESS MSG1 ;LEA TELL COMPUTER WHERE TO START, WE NEED TO STOP SO USE DOLLAR SIGN 
 INT 21H

 ;--OUTPUT STRING:09H
 MOV AH,09H
 LEA DX,MENU ;DISPLAY STRING ,START FROM ADDRESS MSG1 ;LEA TELL COMPUTER WHERE TO START, WE NEED TO STOP SO USE DOLLAR SIGN
 INT 21H


 MOV AH,4CH
 INT 21H
MAIN ENDP 
 END MAIN