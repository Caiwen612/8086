.MODEL SMALL
.STACK 100 
.DATA
    CHAR DB ?
    PRMOPT DB "ENTER A CAPITAL LETTTER: $"
    RESULT DB "THE LOWERCASE LETTER IS: $"
.CODE

MAIN PROC
 MOV AX,@DATA
 MOV DS,AX

 ;OUTPUT STRING:09H (#ENTER A CAPITAL LETTTER:)
 MOV AH,09H
 LEA DX,PRMOPT
 INT 21H

 ;INPUT BYTE:01H (#A)
 MOV AH,01H ;USER KEY IN DATA , THE DATA WILL STORE AL 
 INT 21H ;AL = WILL HOLD USER DATA.
 MOV CHAR,AL ;ASSIGN AL VALUE TO CHAR VARIABLE

 ;NEW LINE (\N)
 MOV AH,02H
 MOV DL,0DH ;MOV DL,13
 INT 21H
 MOV DL,0AH ;MOV DL,10
 INT 21H

 ;OUTPUT STRING:09H (#THE LOWERCASE LETTER IS:)
 MOV AH,09H
 LEA DX,RESULT
 INT 21H

 ;OUTPUT BYTE:02H (CHAR = CHAR + 20H) (CHAR = CHAR +32)
 ;41-5A=A-Z @@@@@ 61-7A=a-z @@@@ 
 ADD CHAR,20H ;CHAR = CHAR + 20H
 MOV AH,02H 
 MOV DL,CHAR ;ASSIGN CHAR VARIABLE TO DL
 INT 21H 

 MOV AH,4CH
 INT 21H
MAIN ENDP
 END MAIN