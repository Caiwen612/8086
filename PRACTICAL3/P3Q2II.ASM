.MODEL SMALL 
.STACK 100 
.DATA 
    CHAR DB ?
    PROMPT DB "ENTER A LOWER LETTTER: $"
    RESULT DB "THE UPPERCASE LETTER IS: $"
.CODE 
MAIN PROC 
 MOV AX,@DATA 
 MOV DS,AX 

 ;OUTPUT STRING:09H
 MOV AH,09H
 LEA DX,PROMPT
 INT 21H

 ;INPUT BYTE:01H
 MOV AH,01H
 INT 21H
 MOV CHAR,AL

 ;NEWLINE
 MOV AH,02H
 MOV DL,0DH
 INT 21H
 MOV DL,0AH
 INT 21H

 ;OUTPUT STRING:09H
 MOV AH,09H
 LEA DX,RESULT
 INT 21H

 ;OUTPUT BYTE:02H
 SUB CHAR,20H
 MOV AH,02H 
 MOV DL,CHAR
 INT 21H

 MOV AH,4CH 
 INT 21H 
MAIN ENDP 
 END MAIN